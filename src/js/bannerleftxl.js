$('#banner_l>ul>li:eq(0)').mouseenter(function(){
	$(this).css({"box-shadow":"0px 1px 0px 0px #cdcdcd"})
	$('#banner_l>ul>li:eq(0)>ul').css({"display":"block","box-shadow":"-1px 1px 0px 0px #cdcdcd"})
}).mouseleave(function(){
	$(this).css({"box-shadow":""})
	$('#banner_l>ul>li:eq(0)>ul').css("display","none")
})


